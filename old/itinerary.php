<h1>Cedar Creek Veterans Foundation</h1>
            <p><img src="assets/layout/CCVFlogo.jpg" alt="Cedar Creek Veterans Foundation" width="131" height="93" align="left" /><img src="assets/layout/best_logo.jpg" width="75" height="125" /><br />
              The CCVF foundation has been established to raise monies to assist with the physical and emotional recovery and rehabilitation of wounded, injured and disabled military personnel and veterans with a focus on those individuals residing in and organizations serving the Northeast Texas region. The foundation’s principal fundraising activity is the Thunder Over Cedar Creek Lake air show presented during the July 4th Holiday timeframe each year. The CCVF is a non-profit 501(c)(3) organization. Your donation is tax-deductible as allowed by law. More information regarding the CCVF can be found at <a href="http://www.ccveteransfoundation.org" target="_blank">www.CCVeteransFoundation.org</a>.<br />
            </p>
